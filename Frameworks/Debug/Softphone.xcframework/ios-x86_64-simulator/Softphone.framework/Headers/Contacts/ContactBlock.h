    class ContactBlockId
    //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
    {
    public:
        ContactBlockId() = default;
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        ContactBlockId(ali::uint32 block, int bitCount)
        :mBlock(block & ((1 << bitCount) - 1) ), mBitCount(bitCount)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        ContactBlockId(ali::blob const& block, int bitCount)
        :mBlock( extractInt(block.data(), block.size(),bitCount) ), mBitCount(bitCount)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {}
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        ContactBlockId(ali::string const& blockAsHexString, int bitCount)
        :mBlock( extractInt(blockAsHexString, bitCount) ), mBitCount(bitCount)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {}
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        bool operator == (ContactBlockId const& rhs) const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return mBlock == rhs.mBlock && mBitCount == rhs.mBitCount;
        }

        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        bool operator != (ContactBlockId const& rhs) const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return !operator==(rhs);
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        friend int compare(ContactBlockId const& lhs, ContactBlockId const& rhs)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            ali_assert(lhs.mBitCount == rhs.mBitCount);
            
            using ali::compare;
            
            return  (lhs.mBitCount == rhs.mBitCount)
                ?   (compare(lhs.mBlock, rhs.mBlock))
                :   (compare(lhs.mBitCount, rhs.mBitCount));
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        void swap(ContactBlockId & other)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            using ali::swap;
            
            ali::swap(mBitCount,other.mBitCount);
            ali::swap(mBlock,other.mBlock);
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        friend void swap(ContactBlockId & a, ContactBlockId & b)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            a.swap(b);
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        bool matches(ali::blob const& block, int bitLen) const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return ContactBlockId(block,bitLen) == *this;
        }

        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        bool matches(ali::string const& block, int bitLen) const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return ContactBlockId(block,bitLen) == *this;
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        ali::uint32 getBlock() const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return mBlock;
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        int getBitCount() const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return mBitCount;
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        bool isValid() const
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            return mBitCount > 0;
        }

        static ContactBlockId fromString(ali::string const& str);
        
        ali::string toString() const;
        
    private:
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        static ali::uint32 extractInt(ali::string const& blockAsHexString, int bitCountToExtract)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            const int nChars = bitCountToExtract <= 8 ? 2 : bitCountToExtract <= 16 ? 4 : 8;

            ali_assert(blockAsHexString.size() >= nChars);

            ali::uint32 ret = 0;
            
            for( int i=0; i<nChars; ++i )
            {
                ali::uint8 digit;
                ali::parse_digit(digit, blockAsHexString[i], 16);
                
                ret = (ret << 4 ) | digit;
            }

            const int correction = nChars * 4 - bitCountToExtract;
            return ret >> correction;
        }
        
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        static ali::uint32 extractInt(unsigned char const* data, int ALI_IF_DEBUG(dataLen), int bitCountToExtract)
        //-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
        {
            ali_assert(bitCountToExtract <= 32);
            
            const int nBytes = bitCountToExtract <= 8 ? 1 : bitCountToExtract <= 16 ? 2 : 4;
            
            ali_assert(nBytes <= dataLen);

            ali::uint32 ret = 0;
            
            for( int i=0; i<nBytes; ++i )
            {
                ret = (ret << 8) | data[i];
            }
            
            const int correction = nBytes * 8 - bitCountToExtract;
            return ret >> correction;
        }
        
    private:
        ali::uint32     mBlock;
        int             mBitCount;
    };
    
